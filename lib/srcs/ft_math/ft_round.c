long	ft_round(double n)
{
	long	rn;

	if (n >= 0)
	{
		if ((n - (long)n) * 10 > 5)
			rn = (long)n + 1;
		else
			rn = (long)n;
	}
	else
	{
		if ((n - (long)n) * -10 > 5)
			rn = (long)n - 1;
		else
			rn = (long)n;
	}
	return (rn);
}
