#include <stddef.h>

void	*ft_memchr(const void *s, int c, size_t len)
{
	unsigned char	*cur;

	cur = (unsigned char *)s;
	while (len--)
		if (*cur++ == (unsigned char)c)
			return (--cur);
	return (0);
}
