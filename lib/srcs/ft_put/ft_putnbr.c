#include "ft_put.h"

void	ft_putnbr(int n)
{
	long	num;

	num = (long)n;
	if (num < 0)
	{
		ft_putchar('-');
		num = num * -1;
	}
	if (num > 9)
		ft_putnbr(num / 10);
	ft_putchar((num % 10) + 48);
}
